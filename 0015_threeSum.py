'''
给定一个包含 n 个整数的数组 nums，判断 nums 中是否存在三个元素 a，b，c ，使得 a + b + c = 0 ？找出所有满足条件且不重复的三元组。

注意：答案中不可以包含重复的三元组。

例如, 给定数组 nums = [-1, 0, 1, 2, -1, -4]，

满足要求的三元组集合为：
[
  [-1, 0, 1],
  [-1, -1, 2]
]
'''


def threeSum(nums):
    if not nums and len(nums) < 3: return None
    nums.sort()
    i = 0
    l = 1
    r = len(nums) - 1
    result = set()
    while i < len(nums) - 2:
        if nums[i] + nums[l] + nums[r] == 0:
            result.add((nums[i], nums[l], nums[r]))
            # 处理重复数据
            while i < r and nums[i] == nums[i + 1]:
                i += 1
            while r > l and nums[r] == nums[r - 1]:
                r -= 1
            while r > l and nums[l] == nums[l + 1]:
                l += 1
            r -= 1
            l += 1
        if nums[i] + nums[l] + nums[r] > 0:
            r -= 1
        if nums[i] + nums[l] + nums[r] < 0:
            l += 1

        if l >= r:
            i += 1
            l = i + 1
            r = len(nums) - 1

    return result


r = threeSum([0, 0, 0])
assert set([(0, 0, 0)]) == r

r = threeSum([-1, 0, 1, 2, -1, -4])
assert set([(-1, -1, 2), (-1, 0, 1)]) == r

r = threeSum([-2, -3, 0, 0, -2])
assert set([]) == r

r = threeSum([-2, 0, 1, 1, 2])
assert set([(-2, 1, 1), (-2, 0, 2)]) == r

